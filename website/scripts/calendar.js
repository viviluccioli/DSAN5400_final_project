function addToneInfoToDetail(year, month) {
  // Convert to strings to match JSON format
  const yearStr = year.toString();
  const monthStr = month.toString();
  
  if (toneData.avg_tone[yearStr] && toneData.avg_tone[yearStr][monthStr]) {
      const toneValue = toneData.avg_tone[yearStr][monthStr];
      
      // Calculate percentage for progress bar based on the narrower range
      const minTone = -3.5;
      const maxTone = -2.0;
      const range = maxTone - minTone;
      
      // Calculate percentage (value of 0 = 0%, value of -3.5 = 0%, value of -2.0 = 100%)
      let percentage = ((toneValue - minTone) / range) * 100;
      // Clamp to 0-100 range
      percentage = Math.max(0, Math.min(100, percentage));
      
      // Get the color for this tone value
      const toneColor = getToneColor(year, month);
      
      // Create a description based on the tone value
      let toneDescription = "";
      if (toneValue <= -3.0) {
          toneDescription = "Highly negative coverage";
      } else if (toneValue <= -2.5) {
          toneDescription = "Moderately negative coverage";
      } else {
          toneDescription = "Slightly negative coverage";
      }
      
      return `<div class="mt-3">
          <h6>Media Tone Analysis</h6>
          <div class="progress" style="height: 25px;">
              <div class="progress-bar" role="progressbar" 
                  style="width: ${percentage}%; 
                  background-color: ${toneColor}; 
                  color: #000; 
                  font-weight: bold;" 
                  aria-valuenow="${toneValue}" 
                  aria-valuemin="${minTone}" 
                  aria-valuemax="${maxTone}">
                  ${toneValue.toFixed(2)}
              </div>
          </div>
          <div class="d-flex justify-content-between mt-1 small text-muted">
              <span>More Negative (-3.5)</span>
              <span>Less Negative (-2.0)</span>
          </div>
          <p class="mt-2">${toneDescription}</p>
      </div>`;
  }
  
  return '<div class="mt-3"><p class="text-muted">No tone data available for this month.</p></div>';
}

